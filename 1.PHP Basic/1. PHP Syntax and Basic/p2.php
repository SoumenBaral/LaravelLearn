//operator in php
<?php
$a = 10;
$b = 20;        

$sum = $a + $b;

echo "The sum of {$a} and {$b} is {$sum}\n";
echo "The difference of {$b} and {$a} is " . ($b - $a) . "\n";

$division = $b / $a;
echo "The division of {$b} by {$a} is {$division}\n";

$modulus = $b % $a;
echo "The modulus of {$b} by {$a} is {$modulus}\n";

$multiplication = $a * $b;
echo "The multiplication of {$a} and {$b} is {$multiplication}\n";